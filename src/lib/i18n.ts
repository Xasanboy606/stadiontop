interface I18nState {
  lang: Lang;
  setLang: (l: Lang) => void;
}
export const useI18n = create<I18nState>((set) => ({
  lang: (typeof window !== "undefined" && (localStorage.getItem("lang") as Lang)) || "uz",
  setLang: (lang) => {
    if (typeof window !== "undefined") localStorage.setItem("lang", lang);
    set({ lang });
  },
}));

export const useT = () => {
  const lang = useI18n((s) => s.lang);
  return (key: keyof typeof t) => t[key]?.[lang] ?? key;
};
